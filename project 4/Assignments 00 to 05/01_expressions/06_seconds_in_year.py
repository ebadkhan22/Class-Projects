def main():
    days_in_year = 365
    hours_in_day = 24
    minutes_in_hour = 60
    seconds_in_minute = 60

    total_seconds = days_in_year * hours_in_day * minutes_in_hour * seconds_in_minute
    print(f"There are {total_seconds} seconds in a year!")

if __name__ == '__main__':
    main()
